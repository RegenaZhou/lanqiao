/*
问题描述
小蓝特别喜欢蓝色。最近，小蓝学习了颜色在计算机中的一种表示方法：用三个 0 至 255 之间的整数（包含 0 和 255）分别表示颜色的红、绿、蓝三个分量。

在这种颜色的表示方法下，小蓝定义了一种颜色是偏蓝的，是指蓝色分量大于红色分量，且蓝色分量大于绿色分量。
例如，红、绿、蓝分别为 10、10、11 时是偏蓝的；红、绿、蓝分别为 100、200、200 时不是偏蓝的。

小蓝想知道，有多少种不同的颜色是偏蓝的。两种颜色如果在红、绿、蓝中至少有一个分量值不同，就认为是不同的。

输入格式
无。

输出格式
这是一道结果填空的题，你只需要算出结果后提交即可。本题的结果为一个整数，在提交答案时只需要编写一个程序输出这个整数，输出多余的内容将无法得分。*/
#include <iostream>
using namespace std;
using ll = long long;
int main()
{
    ios::sync_with_stdio(0), cin.tie(0), cout.tie(0);
    int ans = 0;
    for (int i = 1; i <= 255; i++)
    {
        ans += i * i;
    }
    cout << ans << '\n';//5559680
    return 0;
}
